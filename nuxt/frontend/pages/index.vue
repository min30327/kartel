<script>
import 'viewport-extra'
import anime from 'animejs'
import { gsap } from "gsap";
import { ScrollTrigger } from 'gsap/ScrollTrigger'
gsap.registerPlugin(ScrollTrigger)

export default {

    async mounted() {
        await this.$nextTick()
        this.titlesAnimation()
        this.scrollAnimation()
        this.IntroAnimation()
    },
    methods :{
        IntroAnimation (){
            const tl = anime.timeline()
            const lead = document.querySelector(".l-hero__lead")
            let pr_str = lead.textContent.split("");
            let str = "";
            pr_str.map( s => {
                str += "<span class='js-str__animation'>" + s  + "</span>";
            })
            
            lead.innerHTML = str;

            const t = lead.querySelectorAll('.js-str__animation')
            tl
            .add({
                targets :'.l-hero__catch svg',
                opacity: [0,1],
                translateX : [30,0],
                easing: "easeOutExpo",
                delay: function(el, i) {
                    return (i * 20);
                },
                duration:1000,
            }, 800)
            .add({
                targets :t,
                opacity: [0,1],
                translateX: ["200%",0],
                easing: "easeInOutExpo",
                delay: function(el, i) {
                    return (i * 20);
                },
                duration:800,
            }, 1500)
            .add({
                targets: '.l-hero__logo--image svg',
                translateX : [30,0],
                opacity: [0,1],
                duration: 800,
                easing: "easeInOutExpo",
                delay: (el,i) => {
                    return i * 40
                }
            },'-=800')
            .add({
                targets: '.l-header__nav',
                right : [-300,0],
                duration: 800,
                easing: "easeOutQuart"
            })
            .add({
                targets: '.l-header__logo',
                translateY : [-200,0],
                duration: 1200,
                easing: "easeOutQuart"
            },'-=600')
        },
        titlesAnimation (){
            const titles = document.querySelectorAll('.c-section__title')

            const setPosition = (window.outerWidth > 991) ? 100 : 50
            if(titles.length > 0){
                titles.forEach(title => {
                    const str = title.querySelector('.c-section__title--str')
                    const lft = title.querySelector('.c-section__title--lft')
                    const rht = title.querySelector('.c-section__title--rht')
                    const svgWrap = title.querySelector('.c-section__title--svg')
                    const svgs = title.querySelectorAll('svg')
                    
                    gsap.from(svgWrap, {
                        opacity:0,
                        scrollTrigger: {
                            trigger: title, 
                            start: 'top center'
                        }
                    })

                    gsap.from(str, {
                        opacity:0,
                        x:-20,
                        delay: .3,
                        scrollTrigger: {
                            trigger: title, 
                            start: 'top center'
                        }
                    })

                    gsap.from(lft, {
                        opacity:0,
                        x:100,
                        y:100,
                        ease:'expo.out',
                        duration: .5,
                        scrollTrigger: {
                            trigger: title, 
                            start: 'top center'
                        }
                    })
                    gsap.from(rht, {
                        opacity:0,
                        x:-100,
                        y:-100,
                        ease:'expo.out',
                        duration: .5, 
                        scrollTrigger: {
                            trigger: title, 
                            start: 'top center'
                        }
                    })
                    if(svgs.length > 0){
                        svgs.forEach(el => {

                            gsap.from(el, { 
                                x: gsap.utils.random([-setPosition, 0, setPosition]), 
                                y: gsap.utils.random([-setPosition,  0, setPosition]), 
                                scale: gsap.utils.random( .3, .7), 
                                duration: gsap.utils.random(.5,1), 
                                delay:gsap.utils.random( .3, .7), 
                                ease:gsap.utils.random(['expo.out','sine.out','back.out']),
                                scrollTrigger: {
                                    trigger: title, 
                                    start: 'top center'
                                }
                            })
                        });
                    }
                });
            }
        },
        scrollAnimation(){
            const els = document.querySelectorAll('[data-scroll-element]')
            if(els.length > 0){
                els.forEach(el => {
                    
                    gsap.from(el, { 
                        y: 20, 
                        opacity: 0, 
                        duration: .5, 
                        ease:'sine.inout',
                        scrollTrigger: {
                            trigger: el, 
                            start: 'top center'
                        }
                    })
                });
            }
        }
    }
}
</script>

